def _localize(entity_id: int, lang: str) -> str:
    html = _EN[entity_id]
    pairs = sorted(
        _ENTITY_TRANS.get(entity_id, {}).get(lang, []),
        key=lambda x: len(x[0]),
        reverse=True,
    )
    for en, loc in pairs:
        html = html.replace(en, loc)
    return html


def get_content(entity_id: int, lang: str) -> str:
    """Return full HTML body matching EN structure exactly."""
    if lang not in ("sw", "ln") or entity_id not in _EN:
        return ""
    return _localize(entity_id, lang)


